# def add(a, b, c):
# return a + b + c


add = (
    lambda a, b, c: a + b + c
)  # this is anonymous function i.e no name so how to call? hence store it in variable

x = add(10, 20, 30)
print(x)


isEven = lambda num: num % 2 == 0


print(isEven(6))
print(isEven(5))

